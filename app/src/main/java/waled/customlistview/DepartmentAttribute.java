package waled.customlistview;
public class DepartmentAttribute {

        public String departmentName;
        public String employeeTotal;
        public String employeeOn;

        //Constructor to pass data to departmentName , employeeTotal , employeeOn
        DepartmentAttribute(String departmentName ,String employeeTotal,String employeeOn ) {

            this.departmentName = departmentName;
            this.employeeTotal = employeeTotal;
            this.employeeOn = employeeOn;

        }


    }

